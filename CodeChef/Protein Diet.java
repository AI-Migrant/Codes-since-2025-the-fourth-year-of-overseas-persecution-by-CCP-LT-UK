import java.util.*;




class Solution
{
    
    public static
    void
    main
    (
        String [] args
    )
    {
        final Scanner scanner = new Scanner( System.in );
        
        if
        (
            scanner.nextInt() >= scanner.nextInt()
        )
        {
            System.out.println( "YES" );
        }
        else
        {
            System.out.println( "NO" );
        }
        
        scanner.close();
    }
    
}
